	public Integer execute() {

		List<InputType> inputTypeList = inputListToInputType();

		inputTypeList = inputTypeList.stream()
				.sorted(Comparator.comparing(e -> e.dateTime))
				.collect(Collectors.toList());

		Integer result = findMostSleepyMinute(inputTypeList);
		return result;
	}

	private Integer findMostSleepyMinute(List<InputType> inputTypeList) {
		Integer guard = 0;
		Integer begin = 0;
		Integer end = 0;
		Map<GuardMinute, Integer> sleepMinutes = new HashMap<>();

		for(InputType entry : inputTypeList) {
			if (entry.action.equals("falls asleep")) {
				begin = entry.dateTime.getMinute();
			}
			else if (entry.action.equals("wakes up")) {
				end = entry.dateTime.getMinute();
				for(int i = begin; i < end; i++) {
					GuardMinute guardMinute = new GuardMinute(guard, i);
					if ( sleepMinutes.containsKey(guardMinute))
						sleepMinutes.replace(guardMinute, sleepMinutes.get(guardMinute)+1);
					else
						sleepMinutes.put(guardMinute, 1);
				}
			}
			else {
				guard = Integer.valueOf(entry.action);
			}
		}

		GuardMinute sleepyGuardMinute = sleepMinutes
				.entrySet()
				.stream()
				.max(Map.Entry.comparingByValue())
				.get()
				.getKey();

		return sleepyGuardMinute.Guard * sleepyGuardMinute.Minute;
	}

	private List<InputType> inputListToInputType() {

		List<InputType> inputTypeList = new ArrayList<>();

		boolean first = true;
		InputType inputType = new InputType();
		for(String entry : inputList) {
			if(first) {
				inputType = new InputType();
				inputType.dateTime = LocalDateTime.parse(entry);
			}
			else {
				inputType.action = entry;
				inputTypeList.add(inputType);
			}
			first = !first;
		}
		return inputTypeList;
	}

